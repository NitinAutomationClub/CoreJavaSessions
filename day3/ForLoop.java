package day3;

public class ForLoop {

	public static void main(String[] args) {
		
	for(int i=0;i<=10;i++)//initilzation, condition ,either increment or decrement
	{
		System.out.println(i);//0 1 2 3
	}
	
	
	System.out.println("Reverse Order");//10,9,8,7,....
	for(int i=10;i>0;i--)
	{
		System.out.println(i);//10 ,9 8 ,7........
	}

	}

}
