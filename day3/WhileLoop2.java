package day3;

public class WhileLoop2 {

	public static void main(String[] args) {
		
	//initilaization ,
		//condition
		//increment or decrement
		
		
		//Print even no : 2, 4, 6, 8,10
		
		int i=2;//initialize
		while(i<=10)//Condition
		{
			System.out.println(i);
			i=i+2;
		}
		System.out.println("Out of Loop");
		
	}

}
